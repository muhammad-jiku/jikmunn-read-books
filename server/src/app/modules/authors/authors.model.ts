import { model, Schema } from 'mongoose';
import { gender } from './authors.constants';
import { IAuthor, IAuthorModel } from './authors.interfaces';

const authorSchema = new Schema<IAuthor, IAuthorModel>(
  {
    id: {
      type: String,
      required: true,
      unique: true,
    },
    name: {
      type: {
        firstName: {
          type: String,
          required: true,
        },
        middleName: {
          type: String,
          required: false,
        },
        lastName: {
          type: String,
          required: true,
        },
      },
      required: true,
    },
    gender: {
      type: String,
      enum: gender,
    },
    dateOfBirth: {
      type: Date,
    },
    email: {
      type: String,
      unique: true,
      required: true,
    },
    contactNo: {
      type: String,
      unique: true,
      required: true,
    },
    presentAddress: {
      type: String,
      required: true,
    },
    permanentAddress: {
      type: String,
      required: true,
    },
    manageBook: {
      type: Schema.Types.ObjectId,
      ref: 'ManageBook',
    },
    profileImage: {
      type: String,
    },
  },
  {
    timestamps: true,
    toJSON: {
      virtuals: true,
    },
  },
);

export const Author: IAuthorModel = model<IAuthor, IAuthorModel>(
  'Author',
  authorSchema,
);
